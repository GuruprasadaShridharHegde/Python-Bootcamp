# All are the example for mutable objects in python
# List Example
my_list = ['1','guru','prasad']
my_list[1] = 'boss'
print(my_list)

# Dictionary Example
my_dict = {'name':'guru','age':'25'}
my_dict['name'] = 'big boss'
print(my_dict)

# Set Example
my_set = {1,2,3}
my_set.add(4)
print(my_set)

print(50+50)
print(0.1+0.2-0.3)